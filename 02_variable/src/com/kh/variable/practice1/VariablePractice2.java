package com.kh.variable.practice1;

public class VariablePractice2 {

	public void practice() {
		int o = 23;
		int t = 7;
		
		System.out.println("첫 번째 정수 : " + o);
		System.out.println("두 번째 정수 : " + t);
		System.out.println();
		System.out.println("더하기 결과 : " + (o+t));
		System.out.println("빼기 결과 : " + (o-t));
		System.out.println("곱하기 결과 : " + (o*t));
		System.out.println("나누기 몫 결과 : "+ (o/t));

	}
	
	
}
